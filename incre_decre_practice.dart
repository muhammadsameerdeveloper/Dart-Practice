void main() {
  var x = 15;
  var y = 6;
  var z = 4;
  var answer =
      ++x *
          y-- //16*6=96
          + //96+0=96
      z++ -
      --y //4-4=0
      + //96+2.7=98.7
      x-- /
          ++z //16/6=2.7
          +
      x %
          --z //15%5=0           // 98.7+0=98.7
          -
      ++x + //98.7-16=82.7        //82.7+20=102.7
      y++ *
          z-- //4*5=20
          +
      --x; //15+102.7=117.7

  print(answer);
}
